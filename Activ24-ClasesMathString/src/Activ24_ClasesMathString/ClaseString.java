
package Activ24_ClasesMathString;


public class ClaseString {


    public static void main(String[] args) {
        String cad1 = "Javadesdecero.es";
        String cad2 = "yavadesdecero.es";
        //length
        System.out.println(cad1.length());
        System.out.println(cad2.length());
        //chartAt
        System.out.println(cad1.charAt(0));
        System.out.println(cad2.charAt(0));
        //equals
        System.out.println(cad1.equals(cad2));
        //substring
        System.out.println(cad1.substring(4));
        System.out.println(cad2.substring(4));
        //toUpperCase
        System.out.println(cad1.toUpperCase());
        System.out.println(cad2.toUpperCase());
        //toLowerCase
        System.out.println(cad1.toLowerCase());
        System.out.println(cad2.toLowerCase());
    }
    
}
